public class PlayingAround {
    public static void main(String arg[]) {
        var a = -5;
        var b = +a;
        System.out.println("b = " + b);

        System.out.println("x = y = 5");
        var x = 5;
        System.out.println("++x " + ++x);
        var y = 5;
        System.out.println("y++ " + y++);
    }
}
